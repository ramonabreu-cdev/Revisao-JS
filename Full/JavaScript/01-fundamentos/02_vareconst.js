
// Usando o var para declarar variável

var idade = 25
/* 
* var(palavra reservada) é o endereço de memória
* idade é o identificador
* = é o simbolo de atribuição, diferente de igualdade.
* 25 é o valor
*/

// usando o let, como forma mais moderna e ampla

let ano = 1995

console.log(idade, ano)

// para constantes uso a mesma estrutura

const nascimento = 0

console.log(nascimento)